    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<String> customerInfo = new LinkedList<>();
        Pattern varchar = Pattern.compile("\\w{1,30}");
        Pattern email = Pattern.compile("\\w@\\w.\\w");
        customerInfo.add(txtNumber.getText());
        customerInfo.add(txtFirst.getText());
        customerInfo.add(txtLast.getText());
        customerInfo.add(txtStreet.getText());
        customerInfo.add(txtCity.getText());
        customerInfo.add(txtState.getText());
        customerInfo.add(txtZip.getText());
        customerInfo.add(txtPhone.getText());
        
        Matcher me = email.matcher(txtEmail.getText());
        if(me.find())
        {
            customerInfo.add(txtEmail.getText());
        }
        
        int checked = 0;
        for(int x = 0; x < customerInfo.size(); x++)
        {
            Matcher mv = varchar.matcher(customerInfo.get(x));
            if(mv.find())
            {
                checked++;
            }
        }
        
        if(checked == 9)
        {
            String commandString = "";
            for(int x = 0; x < customerInfo.size(); x++)
            {
                if(x == customerInfo.size() - 1)
                {
                    commandString += "'" + customerInfo.get(x) + "'";
                }
                else
                {
                    commandString += "'" + customerInfo.get(x) + "',";
                }
            }
            
            final String CONN_STRING = "jdbc:derby://localhost:1527/dbCoffeeStoreData";
            try
            {
                Connection conn = DriverManager.getConnection(CONN_STRING, "root", "root");
                Statement comm = conn.createStatement();
                comm.execute("INSERT INTO customer VALUES(" + commandString + ", 200.0)");
            }
            catch (Exception ex)
            {
                System.out.println("Something went wrong .. " + ex.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Customer has been added!");
        }
        else if (checked == 8)
        {
            JOptionPane.showMessageDialog(null, "Email is not in the correct format.");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Looks like there was a formatting error.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
